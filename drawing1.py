# Functions
def sq(NumberOfSquares, SpacesFromTheLeft, SpacesFromTheTop, Color):
    tom.color(colors[Color])
    tom.pu()
    tom.goto(startX + SpacesFromTheLeft * size, startY - SpacesFromTheTop * size)
    tom.pd()
    tom.begin_fill()
    for i in range(NumberOfSquares):
        for i in range(4):
            tom.fd(size)
            tom.rt(90)
        tom.fd(size)
    tom.end_fill()
    return

# Image
def background():
    tom.goto(startX, startY)
    tom.color('black')
    tom.begin_fill()
    for i in range(2):
        tom.fd(w)
        tom.rt(90)
        tom.fd(h)
        tom.rt(90)
    tom.end_fill()

